def get_events_by_category(category: str) -> list:
    """Return events filtered by category."""
    if category == "全部":
        return HISTORICAL_EVENTS
    return [e for e in HISTORICAL_EVENTS if e["category"] == category]


def get_event_region(event: dict) -> str:
    """Return region label for an event."""
    return EVENT_REGION_MAP.get(event["id"], "全球")


def get_events_by_filters(category: str = "全部", region: str = "全部") -> list:
    """Return events filtered by category and region."""
    events = HISTORICAL_EVENTS
    if category != "全部":
        events = [e for e in events if e["category"] == category]
    if region != "全部":
        events = [e for e in events if get_event_region(e) == region]
    return events


def get_event_by_id(event_id: str) -> dict | None:
    """Return a single event dict by its id, or None if not found."""
    for event in HISTORICAL_EVENTS:
        if event["id"] == event_id:
            return event
    return None


def get_similar_events(event_id: str, n: int = 10) -> list:
    """
    Return up to n events in the same category as event_id,
    sorted by magnitude descending (excluding the event itself).
    """
    target = get_event_by_id(event_id)
    if target is None:
        return []
    same_cat = [
        e for e in HISTORICAL_EVENTS
        if e["category"] == target["category"] and e["id"] != event_id
    ]
    same_cat.sort(key=lambda x: x["magnitude"], reverse=True)
    return same_cat[:n]
